package com.virtualassistant.test;


public class Aeroplane {

    public String flightName ;
    public Float fuelLevel;

    public Integer requiredLandingDistance;


    public String getFlightName() {
        return flightName;
    }

    public void setFlightName(String flightName) {
        this.flightName = flightName;
    }

    public Float getFuelLevel() {
        return fuelLevel;
    }

    public void setFuelLevel(Float fuelLevel) {
        this.fuelLevel = fuelLevel;
    }

    public Integer getRequiredLandingDistance() {
        return requiredLandingDistance;
    }

    public void setRequiredLandingDistance(Integer requiredLandingDistance) {
        this.requiredLandingDistance = requiredLandingDistance;
    }
}
